/* Create a class acc with private data members account_no and balance. provide setter & getter method. Demonstrate how we can update & access the values securely */

public class acc {
    private int account_no, balance;
    
    public void setAccount_no(int account_no) { this.account_no = account_no; }
    public int getAccount_no() { return account_no; }
    public void setBalance(int balance) { this.balance = balance; }
    public int getBalance() { return balance; }
}
